<template>
  <div v-if="isVisible" class="modal">
    <div class="modal-content">
      <span class="close" @click="closeModal">&times;</span>
      <slot></slot>
    </div>
  </div>
</template>

<script setup>
import { ref, defineExpose } from 'vue'

const isVisible = ref(false)

const closeModal = () => {
  isVisible.value = false
}

const showModal = () => {
  isVisible.value = true
}

defineExpose({
  isVisible,
  showModal,
  closeModal
})
</script>

